package modelo.dominio;

import java.util.List;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

@Entity
@Table(name = "tab_situacoes")
public class Situacao {
	
	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "ID_SITUACAO")
	@SequenceGenerator(name = "ID_SITUACAO", sequenceName = "SEQ_SITUACAO", allocationSize = 1)
	private Integer id;

	@NotNull
	@Size(max = 45)
	@Column(length = 45, nullable = false)
	private String nome;

	@Size(max = 60)
	@Column(length = 60)
	private String descricao;
	
	@OneToMany(mappedBy="situacao", fetch=FetchType.LAZY)
	private List<Troca> trocas;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	@Override
	public String toString() {
		return "Situacao [id=" + id + ", nome=" + nome + ", descricao=" + descricao + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Situacao other = (Situacao) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}
